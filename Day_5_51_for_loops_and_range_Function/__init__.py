# Iterate through number 1 to 10.
for number in range(1, 11):
    print(number)

# Iterate through number 1 to 10, increment value is 3.  1 -> 4 -> 7 -> 10.
for number in range(1, 11, 3):
    print(number)

# Sum every number from 1 to 100.
total = 0
for number in range(1, 101):
    total += number;

print(total)
